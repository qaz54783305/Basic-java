
public class ifTest2 {
	public static void main(String[] args) {

		int Score = (int) (Math.random() * 101);
        System.out.println(Score);
		if (Score >= 90) {

			System.out.println("A");

		} else if (Score >= 80) {

			System.out.println("B");
		} else if (Score >= 70) {

			System.out.println("C");
		}

		else if (Score >= 60) {

			System.out.println("D");
		}
		
		   
		
	
	}
}
